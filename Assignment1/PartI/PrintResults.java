
public class PrintResults {

	public static void main(String[] args) {
		
		System.out.println("The value of 1+1 is " + (1+1));
		int a = 10 + 5;
		int b = 50 - 23;
		int c = 12 * 13;
		double d = 20.0 / 3.0;
		int e = 100 % 7;
		double f = Math.pow(4, 3);
		System.out.println("The value of 10+5 is " + (a));
		System.out.println("The value of 50-23 is " + (b));
		System.out.println("The value of 12*13 is " + (c));
		System.out.println("The value of 20.0 / 3.0 is " + (d));
		System.out.println("The value of 100 % 7 is " + (e));
		System.out.println("The value of pow(4,3) is " + (f));

	}
}
